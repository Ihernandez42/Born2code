#include "libft.h"

int	digit_counter(int n)
{
	int	i;
	int	copy;

	i = 0;
	if (n == 0)
		return (1);
	if (n == -2147483648)
		return (11);
	if (n < 0)
	{
		n = -n;
		i++;
	}
	copy = n;
	while (copy > 0)
	{
		copy = copy / 10;
		i++;
	}
	return (i);
}

int	exp_counter(int n)
{
	int	exp;
	int	digits;

	if (n == 0)
		return (1);
	if (n == -2147483648)
		return (1000000000);
	digits = digit_counter(n);
	if (n < 0)
		digits--;
	exp = 1;
	while (--digits)
		exp = exp * 10;
	return (exp);
}

char	*ft_itoa(int n)
{
	char		*str;
	int			exp;
	int			i;
	long int	copy;

	copy = (long int)n; // lo cambiamos a long y lo guardamos en copy para evitar desbordamiento 
	exp = exp_counter(n);
	i = 0;
	str = malloc(digit_counter(n) + 1); // reserva de memoria 
	if (!str)
		return (NULL);

	if (n < 0) 
	{
		str[i++] = '-';
		copy = -copy;
	}
	while (exp > 0)
	{
		str[i++] = (copy / exp) + 48;
		copy = copy % exp;
		exp = exp / 10;
	}
	str[i] = '\0';
	return (str);
}
int main() {
    int number = 12345;

    // Convert integer to string
    char* result = itoa(number);

    // Print the result
    printf("Integer: %d\nString: %s\n", number, result);

    return 0;
}
